
const число1 = parseFloat(prompt('Введіть перше число:'));
const число2 = parseFloat(prompt('Введіть друге число:'));
const число3 = parseFloat(prompt('Введіть третє число:'));


if (isNaN(число1) || isNaN(число2) || isNaN(число3)) {
    alert('Ви ввели некоректне значення. Будь ласка, введіть числа.');
} else {

    const середнє = (число1 + число2 + число3) / 3;


    alert(`Середнє арифметичне введених чисел: ${середнє}`);
}
